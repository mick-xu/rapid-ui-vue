import copy from "copy-to-clipboard";
import Article from "@c/Article";
export default {
  name: "Icons",
  components: { Article },
  setup() {
    const handleClick = (e) => {
      let copyText = "";
      let nameNode;
      if (e.target.className === "name") {
        nameNode = e.target;
        copyText = nameNode.innerText;
      } else {
        if (e.target.className.startsWith("icon iconfont")) {
          nameNode = e.target.nextElementSibling;
        } else if (e.target.className === "dib") {
          nameNode = e.target.lastElementChild;
        }
        copyText = `<Icon name="${nameNode.innerText}" />`;
      }
      copy(copyText);
      const li = nameNode.parentNode;
      const message = document.createElement("div");
      message.className = "message";
      message.innerText = "已复制";
      li.appendChild(message);
      setTimeout(() => {
        li.removeChild(message);
      }, 2000);
    };
    return { handleClick };
  },
};
</script>

